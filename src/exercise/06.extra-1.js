// useEffect: HTTP requests
// http://localhost:3000/isolated/exercise/06.js

import * as React from 'react'
import {
  PokemonForm,
  fetchPokemon,
  PokemonInfoFallback,
  PokemonDataView,
} from '../pokemon'

function PokemonInfo({pokemonName}) {
  const [pokemonInfoState, setPokemonInfoState] = React.useState(null)
  const [error, setError] = React.useState(null)

  React.useEffect(() => {
    if (!pokemonName) {
      return
    }
    setError(null)
    fetchPokemon(pokemonName)
      .then(pokemonData => {
        setPokemonInfoState(pokemonData)
      })
      .catch(error => setError(error))
  }, [pokemonName])

  if (!pokemonName) {
    return 'Submit a pokemon'
  } else if (error) {
    return (
      <div role="alert">
        There was an error:{' '}
        <pre style={{whiteSpace: 'normal'}}>{error.message}</pre>
      </div>
    )
  } else if (pokemonName && !pokemonInfoState) {
    return <PokemonInfoFallback name={pokemonName} />
  } else if (pokemonInfoState) {
    return <PokemonDataView pokemon={pokemonInfoState} />
  }
}

function App() {
  const [pokemonName, setPokemonName] = React.useState('')

  function handleSubmit(newPokemonName) {
    setPokemonName(newPokemonName)
  }

  return (
    <div className="pokemon-info-app">
      <PokemonForm pokemonName={pokemonName} onSubmit={handleSubmit} />
      <hr />
      <div className="pokemon-info">
        <PokemonInfo pokemonName={pokemonName} />
      </div>
    </div>
  )
}

export default App
